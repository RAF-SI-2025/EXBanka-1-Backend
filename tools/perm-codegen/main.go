// perm-codegen reads contract/permissions/catalog.yaml and writes
// contract/permissions/perms.gen.go containing:
//
//   - var Catalog []Permission              — every permission, in catalog order
//   - var <Resource> = struct{ <Verb> ... } — typed nested constants per resource
//   - var DefaultRoles map[string][]Permission — flattened role bundles
//   - func IsValid(p Permission) bool       — membership predicate
//
// The output is gofmt-formatted and deterministic for a given YAML input.
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"io"
	"log"
	"os"
	"sort"
	"strings"

	"github.com/exbanka/contract/permissions"
)

const headerTmpl = `// Code generated by perm-codegen. DO NOT EDIT.
package permissions

`

const footerTmpl = `
var catalogSet = func() map[Permission]struct{} {
	m := make(map[Permission]struct{}, len(Catalog))
	for _, p := range Catalog {
		m[p] = struct{}{}
	}
	return m
}()

// IsValid reports whether p is part of the generated catalog.
func IsValid(p Permission) bool {
	_, ok := catalogSet[p]
	return ok
}
`

// resourceVerbScope groups perms by resource → verb → scope for the typed
// var declarations.
type resourceVerbScope struct {
	Resource string
	Verbs    []verbScopes
}

type verbScopes struct {
	Verb   string
	Scopes []string // distinct scopes for this resource.verb
}

func generate(yamlData []byte, w io.Writer) error {
	cat, err := permissions.ParseYAML(yamlData)
	if err != nil {
		return err
	}

	var buf bytes.Buffer
	buf.WriteString(headerTmpl)

	// Catalog slice — every permission in catalog order.
	buf.WriteString("var Catalog = []Permission{\n")
	for _, p := range cat.All {
		fmt.Fprintf(&buf, "\t%q,\n", string(p))
	}
	buf.WriteString("}\n\n")

	// Group by resource → verb → [scopes] and emit one typed var per resource.
	grouped := groupByResourceVerb(cat.All)
	for _, rvs := range grouped {
		emitResourceVar(&buf, rvs)
	}

	// DefaultRoles map — flattened role bundles, sorted by role name for
	// determinism.
	buf.WriteString("var DefaultRoles = map[string][]Permission{\n")
	roleNames := make([]string, 0, len(cat.Roles))
	for n := range cat.Roles {
		roleNames = append(roleNames, n)
	}
	sort.Strings(roleNames)
	for _, n := range roleNames {
		fmt.Fprintf(&buf, "\t%q: {\n", n)
		for _, p := range cat.Roles[n] {
			fmt.Fprintf(&buf, "\t\t%q,\n", string(p))
		}
		buf.WriteString("\t},\n")
	}
	buf.WriteString("}\n")

	buf.WriteString(footerTmpl)

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Helpful: dump unformatted output for debugging.
		_, _ = w.Write(buf.Bytes())
		return fmt.Errorf("gofmt: %w", err)
	}
	_, err = w.Write(formatted)
	return err
}

func groupByResourceVerb(perms []permissions.Permission) []resourceVerbScope {
	bag := map[string]map[string][]string{}
	for _, p := range perms {
		parts := strings.Split(string(p), ".")
		if len(parts) != 3 {
			// Shouldn't happen — ParseYAML already validated. Defensive.
			continue
		}
		r, v, s := parts[0], parts[1], parts[2]
		if bag[r] == nil {
			bag[r] = map[string][]string{}
		}
		bag[r][v] = append(bag[r][v], s)
	}
	rNames := make([]string, 0, len(bag))
	for r := range bag {
		rNames = append(rNames, r)
	}
	sort.Strings(rNames)
	out := make([]resourceVerbScope, 0, len(rNames))
	for _, r := range rNames {
		vNames := make([]string, 0, len(bag[r]))
		for v := range bag[r] {
			vNames = append(vNames, v)
		}
		sort.Strings(vNames)
		rv := resourceVerbScope{Resource: r}
		for _, v := range vNames {
			scopes := bag[r][v]
			sort.Strings(scopes)
			rv.Verbs = append(rv.Verbs, verbScopes{Verb: v, Scopes: scopes})
		}
		out = append(out, rv)
	}
	return out
}

func emitResourceVar(buf *bytes.Buffer, rvs resourceVerbScope) {
	resName := exportName(rvs.Resource)

	// Type declaration.
	fmt.Fprintf(buf, "var %s = struct {\n", resName)
	for _, vs := range rvs.Verbs {
		fmt.Fprintf(buf, "\t%s struct {\n", exportName(vs.Verb))
		for _, s := range vs.Scopes {
			fmt.Fprintf(buf, "\t\t%s Permission\n", exportName(s))
		}
		buf.WriteString("\t}\n")
	}
	buf.WriteString("}{\n")

	// Initialization.
	for _, vs := range rvs.Verbs {
		fmt.Fprintf(buf, "\t%s: struct {\n", exportName(vs.Verb))
		for _, s := range vs.Scopes {
			fmt.Fprintf(buf, "\t\t%s Permission\n", exportName(s))
		}
		buf.WriteString("\t}{\n")
		for _, s := range vs.Scopes {
			fmt.Fprintf(buf, "\t\t%s: %q,\n", exportName(s),
				rvs.Resource+"."+vs.Verb+"."+s)
		}
		buf.WriteString("\t},\n")
	}
	buf.WriteString("}\n\n")
}

// exportName converts snake_case to PascalCase: "bank_accounts" → "BankAccounts".
// Note: a resource named after a Go reserved keyword would produce an invalid
// identifier; the current catalog has no such names. The codegen's gofmt step
// would surface that as a compile error.
func exportName(s string) string {
	parts := strings.Split(s, "_")
	for i, p := range parts {
		if p == "" {
			continue
		}
		parts[i] = strings.ToUpper(p[:1]) + p[1:]
	}
	return strings.Join(parts, "")
}

func main() {
	in := "contract/permissions/catalog.yaml"
	out := "contract/permissions/perms.gen.go"
	if len(os.Args) > 1 {
		in = os.Args[1]
	}
	if len(os.Args) > 2 {
		out = os.Args[2]
	}

	data, err := os.ReadFile(in)
	if err != nil {
		log.Fatalf("read %s: %v", in, err)
	}

	var buf bytes.Buffer
	if err := generate(data, &buf); err != nil {
		log.Fatalf("generate: %v", err)
	}
	if err := os.WriteFile(out, buf.Bytes(), 0o644); err != nil {
		log.Fatalf("write %s: %v", out, err)
	}
	fmt.Printf("wrote %s\n", out)
}
